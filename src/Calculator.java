public class Calculator {
	

	public double add(double x, double y) {
		double z = x + y;
		

		return z;
	}

	public double subt(double x, double y) {
		double z = x - y;
		
		return z;
	}

	public double multi(double x, double y) {
		double z = x*y;
		
		return z;
	}

	public double div(double x, double y) {
		double z = x/y;
		
		return z;
	}
}